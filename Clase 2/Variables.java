public class Variables{
    public static void main(String[]args){
        
        //Declaración de una variable de tipo entero
        int edad = 25;
        
        //Declaración de una variable de tipo real.
        double altura = 1.75;
        
        //Declaración de una variable de tipo caden
        String nombre = "Eidan";
        
        //Declaración de una variable de tipo boolean
        boolean esEstudiante = true;
        
        /* 
        Con la instruccion System.out.println se mostraran
        Las variables y cada una de ellas estará concatenadas con el signo (+)
        y la leyenda. 
        */
        System.out.println("Nombre: " + nombre + ",Edad: " + edad + ", Altura: " + altura);
        System.out.println("Estudiante" + esEstudiante);

    }
}
